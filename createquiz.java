    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/quiz", "root", "");
            String quizName = request.getParameter("name").replaceAll("\\s+", "_").toLowerCase();
            Statement stmt = con.createStatement();
            String sql = "CREATE TABLE Quiz_" + quizName + " (" +
                       "questid INT PRIMARY KEY AUTO_INCREMENT," +
                       "question TEXT," +
                       "opt1 VARCHAR(255)," +
                       "opt2 VARCHAR(255)," +
                       "opt3 VARCHAR(255)," +
                       "opt4 VARCHAR(255)," +
                       "correct_ans VARCHAR(255)," +
                       "marks INT)";
            stmt.executeUpdate(sql);

            response.getWriter().write("Table '" + quizName + "' created successfully.");
            con.close();
        } catch (Exception e) {
        response.getWriter().write("Error: " + e.getMessage());
        }
    }

